/**
 * add - addit
 * @a: num
 * @b: num
 * Return: result
 */
int add(int a, int b)
{
      return (a + b);
}
/**
 * sub - addit
 * @a: num
 * @b: num
 * Return: result
 */
int sub(int a, int b)
{
      return (a - b);
}
/**
 * mul - addit
 * @a: num
 * @b: num
 * Return: result
 */
int mul(int a, int b)
{
      return (a * b);
}
/**
 * div - addit
 * @a: num
 * @b: num
 * Return: result
 */
int div(int a, int b)
{
      return (a / b);
}
/**
 * mod - addit
 * @a: num
 * @b: num
 * Return: result
 */
int mod(int a, int b)
{
      return (a % b);
}
